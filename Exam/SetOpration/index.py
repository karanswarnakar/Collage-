A = {1,2,3,4,5}
B = {2,5,9,10,4}

print("Original Sets:")
print(A)
print(B, "\n")

# Union

# A | B 

print("Union Opration:")
print(A.union(B))
print("\n")


# Intersection

# A & B

print("Intersection Opration:")
print(A.intersection(B))
print("\n")


# Differense

# A - B

print("Differense Opration:")
print(A.difference(B))
print("\n")


# IsSuBset

# A <= B

print("IsSubset or not Opration:")
print(A.issubset(B))
print("\n")

# IsSupper set or not 

# A >= B

print("IsSuper or not Opration:")
print(A.issuperset(B))
print("\n")